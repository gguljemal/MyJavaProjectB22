package day05_primitives_concatanation;

public class PrimitiveExamples {

    public static void main (String [] args){

        //*** WHOLE NUMBERS/INTEGER TYPES****
        byte byteValue = 100;
        short shortValue = 32000;
        int intValue = 23258269;
        long longValue = 2589987962314L;

        //*****DECIMAL NUMBERS/FLOATING POINT TYPES*****
        float floatValue = 2358.417F;
        double doubleValue = 4576891.64578;

        //*****CHARACTER TYPE******
        char charType = 'a';
        boolean booleanValue = true; // or false

        System.out.println("booleanValue = " + booleanValue);
        System.out.println("charType = " + charType);
        System.out.println("doubleValue = " + doubleValue);
        System.out.println("floatValue = " + floatValue);
        System.out.println("longValue = " + longValue);
        System.out.println("intValue = " + intValue);
        System.out.println("shortValue = " + shortValue);
        System.out.println("byteValue = " + byteValue);


    }
}
