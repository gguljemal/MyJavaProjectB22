package day03_escape_sequence;

public class EscapeSequence {

    public static void main(String[] args) {

        System.out.println(" I love \"Java\" programming");
        System.out.println("Lets buy \"tea\" and \"coffee\" ");
        System.out.println("java\nintelij");
        System.out.println("\tjava\tselenium\tapi");

        System.out.println(" \"iphone case\", \"iphone\", \"xbox\", \"webcam\", \"monitor\"");

        System.out.println("   ^");
        System.out.println(" /   \\");
        System.out.println(" |   |");
        System.out.println(" -----");

        System.out.println("Company name:\tGoogle\nEmployee:\t\tKhalid\nID:\t\t\t\t12435\nJob Title:\t\tSDET");
    }
}
