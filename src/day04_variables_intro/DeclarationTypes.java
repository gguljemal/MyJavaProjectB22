package day04_variables_intro;

public class DeclarationTypes {

  public static void main(String [] arg){

      //declaration + assignment in single statement:
      int bananas = 34;
      System.out.println("bananas = " + bananas);

      int students = 400;
      System.out.println("students = " + students);

      int peopleInCity, birdsOnTree, fishInLake;

      peopleInCity = 10000;
      birdsOnTree = 21;
      fishInLake = 100;

      System.out.println(peopleInCity);
      System.out.println(birdsOnTree);
      System.out.println(fishInLake);

      int studentNY=58, studentVA = 200;
      System.out.println("studentNY = " + studentNY);
      System.out.println("studentVA = " + studentVA);



  }
}
