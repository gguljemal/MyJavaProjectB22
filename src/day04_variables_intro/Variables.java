package day04_variables_intro;

public class Variables {

    public static void main (String [] arg){

        //declare variable n that can store int(whole numbers)
        int n;
        n=10;
        System.out.println(n);

        //declare variable bananas, assign 100, print value

        int bananas = 100;
        System.out.println(bananas);

        //declare variable students that stores numbers

        int students;
        students=50;
        System.out.println("students = " + students);
        students=70;
        System.out.println("students = " + students);

        int teachers;
        teachers=2;
        int mentors;
        mentors=25;
        System.out.println("Number of teachers:");
        System.out.println("teachers = " + teachers);//2

        System.out.println("Number of mentors:");
        System.out.println("mentors = " + mentors);
        System.out.println("students = " + students);

    }
}
